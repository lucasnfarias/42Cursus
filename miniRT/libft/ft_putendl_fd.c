#include "libft.h"
#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	char lb;

	lb = '\n';
	while (s && *s)
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, &lb, 1);
}
